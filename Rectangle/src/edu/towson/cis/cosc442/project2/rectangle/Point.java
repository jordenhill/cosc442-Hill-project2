package edu.towson.cis.cosc442.project2.rectangle;

/**
 * The Point Class.
 */
public class Point {
	
	/** xCoord and yCoord coordinates. */
	public Double xCoord, yCoord;
	
	/**
	 * Instantiates a new point.
	 *
	 * @param xCoord the xCoord
	 * @param yCoord the yCoord
	 */
	Point(Double xCoord, Double yCoord) {
		this.xCoord = xCoord;
		this.yCoord = yCoord;
	}
}
